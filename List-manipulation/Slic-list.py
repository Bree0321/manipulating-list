my_list = [10, 20, 30, 40, 50, 60]

# Slicing a list
print("Elements from index 1 to 3 (exclusive):", my_list[1:3]) 
print("Elements from the beginning to index 3 (exclusive):", my_list[:3])   
print("Elements from index 2 to the end:", my_list[2:])   
print("Every other element:", my_list[::2])  
print("Reversed list:", my_list[::-1]) 